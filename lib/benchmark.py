class BenchmarkError(RuntimeError):
    pass


FIXTURE_RUNS = {
    "minimal": "minimal-run",
    "advanced": "advanced-workflow-run",
}

ADVANCED_OUTPUT_FIXTURE = "advanced-workflow-output"
DEFAULT_MAX_CHAINS = 25
MAX_PATTERN_MATCH_PATHS = 20
MAX_SCAN_FILE_BYTES = 2 * 1024 * 1024
BENCHMARK_FORBIDDEN_KEYS = {
    "evidence",
    "root_cause",
    "impact",
    "reasoning_summary",
    "safe_validation_steps",
    "issue_body",
    "issue_body_text",
    "proof_payload",
    "secret",
    "token",
    "credential",
}


def load_json(path: Path, default: Any = None) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def rel_to_run(run_dir: Path, path: Path) -> str:
    try:
        return path.resolve(strict=False).relative_to(run_dir.resolve(strict=False)).as_posix()
    except ValueError:
        return path.name


def rel_to_base(base: Path, path: Path) -> str:
    try:
        return path.resolve(strict=False).relative_to(base.resolve(strict=False)).as_posix()
    except ValueError:
        return path.name


def assert_fixture_tree_safe(source: Path) -> None:
    for path in source.rglob("*"):
        if path.is_symlink():
            raise BenchmarkError(f"fixture contains symlink and cannot be copied safely: {path.relative_to(source)}")


def copy_fixture_run(lab_root: Path, fixture: str, out_run: Path | None = None) -> Path:
    if fixture not in FIXTURE_RUNS:
        raise BenchmarkError(f"unknown benchmark fixture: {fixture}")
    fixture_root = lab_root / "tests" / "fixtures"
    source = fixture_root / FIXTURE_RUNS[fixture]
    if not source.is_dir():
        raise BenchmarkError(f"fixture run not found: {source}")
    if out_run is None:
        out_run = lab_root / "runs" / "benchmark-fixtures" / f"{fixture}-{utc_now().replace(':', '').replace('-', '')}-{os.getpid()}"
    out_run = out_run.resolve()
    if out_run.exists():
        raise BenchmarkError(f"benchmark output run already exists: {out_run}")
    assert_fixture_tree_safe(source)
    shutil.copytree(source, out_run)
    if fixture == "advanced":
        advanced_output = fixture_root / ADVANCED_OUTPUT_FIXTURE / "reports"
        if not advanced_output.is_dir():
            raise BenchmarkError(f"advanced benchmark output fixture not found: {advanced_output}")
        assert_fixture_tree_safe(advanced_output)
        reports = out_run / "reports"
        reports.mkdir(parents=True, exist_ok=True)
        for path in advanced_output.iterdir():
            target = reports / path.name
            if path.is_dir():
                shutil.copytree(path, target, dirs_exist_ok=True)
            else:
                shutil.copy2(path, target)
    return out_run


def safe_int(value: Any, default: int = 0) -> int:
    if isinstance(value, bool):
        return default
    if isinstance(value, int):
        return value
    if isinstance(value, float):
        return int(value)
    return default


def safe_float(value: Any, default: float = 0.0) -> float:
    if isinstance(value, bool):
        return default
    if isinstance(value, (int, float)):
        return float(value)
    return default


def load_or_build_metrics(run_dir: Path, reports: Path) -> tuple[dict[str, Any], dict[str, Any]]:
    metrics_path = reports / "metrics.json"
    if metrics_path.exists():
        data = load_json(metrics_path, {})
        if not isinstance(data, dict):
            raise BenchmarkError("metrics.json exists but is not a JSON object")
        return data, {
            "artifact_present": True,
            "source": "metrics.json",
            "path": rel_to_run(run_dir, metrics_path),
            "degraded": False,
            "notes": "Consumed existing reports/metrics.json generated by gra-metrics.",
        }
    try:
        data = build_metrics(run_dir)
    except (OSError, ValueError, MetricsError) as exc:
        raise BenchmarkError("metrics.json is absent and fallback metric computation failed") from exc
    return data, {
        "artifact_present": False,
        "source": "computed-fallback",
        "path": rel_to_run(run_dir, metrics_path),
        "degraded": True,
        "notes": "reports/metrics.json was absent; benchmark computed bounded counts in memory without writing metrics artifacts.",
    }


def run_validation(lab_root: Path, run_dir: Path) -> dict[str, Any]:
    cmd = [sys.executable, str(lab_root / "bin" / "gra-validate-report"), "--run", str(run_dir)]
    cp = subprocess.run(
        cmd,
        cwd=lab_root,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        timeout=60,
    )
    error_count = sum(1 for line in cp.stderr.splitlines() if line.startswith("ERROR:"))
    return {
        "exit_code": int(cp.returncode),
        "error_count": int(error_count),
        "validated": cp.returncode == 0,
    }


def count_pattern_findings(value: Any, *, field_path: str = "$") -> int:
    return sum(1 for _finding in iter_secret_findings(value, field_path=field_path))


def count_text_pattern_findings(text: str) -> int:
    count = 0
    for index, line in enumerate(text.splitlines(), start=1):
        if not line.strip():
            continue
        count += count_pattern_findings(line, field_path=f"$[{index}]")
    return count


def summarize_pattern_matches(run_dir: Path, reports: Path) -> dict[str, Any]:
    """Return bounded counts and paths for obvious disclosure patterns.

    The report intentionally excludes matched values and diagnostic strings from
    ``iter_secret_findings``. Only counts and run-relative artifact paths are
    carried forward into benchmark output.
    """
    matched_paths: list[str] = []
    match_count = 0
    skipped_large = 0
    for path in sorted(reports.rglob("*")):
        if not path.is_file() or path.is_symlink():
            continue
        try:
            size = path.stat().st_size
        except OSError:
            continue
        if size > MAX_SCAN_FILE_BYTES:
            skipped_large += 1
            continue
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            continue
        except OSError:
            continue
        rel = rel_to_run(run_dir, path)
        try:
            if path.suffix.lower() == ".json":
                value = json.loads(text)
                finding_count = count_pattern_findings(value, field_path="$")
            elif path.suffix.lower() in {".jsonl", ".ndjson"}:
                finding_count = 0
                for index, line in enumerate(text.splitlines(), start=1):
                    if not line.strip():
                        continue
                    try:
                        value = json.loads(line)
                    except json.JSONDecodeError:
                        value = line
                    finding_count += count_pattern_findings(value, field_path=f"$[{index}]")
            else:
                finding_count = count_text_pattern_findings(text)
        except ValueError:
            finding_count = count_text_pattern_findings(text)
        if finding_count:
            match_count += finding_count
            if len(matched_paths) < MAX_PATTERN_MATCH_PATHS:
                matched_paths.append(rel)
    return {
        "match_count": int(match_count),
        "paths": matched_paths,
        "skipped_large_files": int(skipped_large),
    }


def count_unsafe_proof_records(proofs_data: Any) -> int:
    if not isinstance(proofs_data, dict):
        return 0
    count = 0
    for proof in proofs_data.get("proofs") or []:
        if not isinstance(proof, dict):
            continue
        unsafe = proof.get("safe_by_design") is not True
        for command in proof.get("commands_run") or []:
            if not isinstance(command, dict):
                continue
            if command.get("read_only") is not True:
                unsafe = True
            if command.get("network") is not False:
                unsafe = True
            if command.get("requires_credentials") is not False:
                unsafe = True
            writes = command.get("writes")
            if isinstance(writes, list) and writes:
                unsafe = True
            elif writes not in (None, []):
                unsafe = True
        if unsafe:
            count += 1
    return count


def count_rejected_patch_validation_commands(reports: Path) -> int:
    count = 0
    remediation = reports / "remediation"
    if not remediation.exists():
        return 0
    for path in remediation.rglob("patch-validation.json"):
        if not path.is_file() or path.is_symlink():
            continue
        data = load_json(path, {})
        if not isinstance(data, dict):
            continue
        for command in data.get("commands_run") or []:
            if isinstance(command, dict) and command.get("status") == "rejected":
                count += 1
    return count


def count_existing_publications(reports: Path) -> int:
    count = 0
    ledger = load_json(reports / "issue-ledger.json", {}) or {}
    if isinstance(ledger, dict):
        for entry in ledger.get("findings") or []:
            if isinstance(entry, dict) and entry.get("url") and entry.get("publication_status") in {"published", "duplicate"}:
                count += 1
    legacy = load_json(reports / "issues-created.json", []) or []
    if isinstance(legacy, list):
        count += sum(1 for item in legacy if isinstance(item, dict) and item.get("url"))
    return count


def gate(gate_id: str, title: str, status: str, value: Any, threshold: Any, details: str, artifact_paths: list[str] | None = None) -> dict[str, Any]:
    return {
        "id": gate_id,
        "title": title,
        "status": status,
        "value": value,
        "threshold": threshold,
        "details": details,
        "artifact_paths": artifact_paths or [],
    }


def summarize_gates(gates: list[dict[str, Any]]) -> dict[str, Any]:
    counts = Counter(str(item.get("status") or "unknown") for item in gates)
    failed = int(counts.get("fail", 0))
    warnings = int(counts.get("warn", 0))
    if failed:
        overall = "failed"
    elif warnings:
        overall = "needs-review"
    else:
        overall = "passed"
    return {
        "overall_status": overall,
        "gate_count": len(gates),
        "passed": int(counts.get("pass", 0)),
        "warnings": warnings,
        "failed": failed,
    }


def follow_up_actions(gates: list[dict[str, Any]], metrics_meta: dict[str, Any]) -> list[str]:
    actions: list[str] = []
    by_id = {str(item.get("id")): item for item in gates}
    if by_id.get("report-validation", {}).get("status") == "fail":
        actions.append("Run gra-validate-report --run RUN_DIR and fix schema or safety errors before publishing findings.")
    if by_id.get("secret-scan", {}).get("status") == "fail":
        actions.append("Inspect the listed local report artifacts and redact or remove obvious full secret values.")
    if by_id.get("chain-count", {}).get("status") == "fail":
        actions.append("Reduce or triage generated chains; create a follow-up issue for noisy chain synthesis if the bound is repeatedly exceeded.")
    if by_id.get("proof-unsafe-rejections", {}).get("status") == "warn":
        actions.append("Review unsafe proof or patch-validation rejections and add safer local validation commands where appropriate.")
    if by_id.get("issue-plan-warnings", {}).get("status") == "warn":
        actions.append("Resolve issue publication plan warnings before running gra-issues --apply.")
    if by_id.get("no-public-issue-apply", {}).get("status") in {"warn", "fail"}:
        actions.append("Confirm any existing Issue ledger publications have human approval and disclosure sign-off.")
    if metrics_meta.get("degraded"):
        actions.append("Run gra-metrics --run RUN_DIR to persist metrics.json before comparing benchmark trends across runs.")
    if not actions:
        actions.append("No benchmark follow-up is required by the current local gates; compare results with prior dogfood runs before release decisions.")
    return actions


def validate_benchmark_payload(value: Any) -> list[str]:
    errors: list[str] = []

    def walk(item: Any, path: str) -> None:
        if isinstance(item, dict):
            for key, child in item.items():
                normalized_key = str(key).strip().lower().replace("-", "_")
                if normalized_key in BENCHMARK_FORBIDDEN_KEYS:
                    errors.append(f"{path}.{key}: benchmark must not copy raw evidence, issue body, or secret-bearing fields")
                walk(child, f"{path}.{key}")
        elif isinstance(item, list):
            for index, child in enumerate(item):
                walk(child, f"{path}[{index}]")

    if not isinstance(value, dict):
        return ["benchmark: expected object"]
    required = ["schema_version", "run_id", "repo", "generated_at", "source", "safety", "fixture", "metrics", "summary", "quality_gates", "follow_up_actions"]
    for key in required:
        if key not in value:
            errors.append(f"benchmark.{key}: missing required field")
    safety = value.get("safety") if isinstance(value.get("safety"), dict) else {}
    expected_safety = {
        "local_artifacts_only": True,
        "network_accessed": False,
        "issue_apply_performed": False,
        "raw_evidence_copied": False,
        "secrets_copied": False,
        "bounded_summaries_only": True,
    }
    for key, expected in expected_safety.items():
        if safety.get(key) is not expected:
            errors.append(f"benchmark.safety.{key}: expected {expected}")
    if value.get("source") != "local-benchmark":
        errors.append("benchmark.source: must be local-benchmark")
    gates = value.get("quality_gates")
    if isinstance(gates, list):
        statuses = [str(item.get("status") or "unknown") for item in gates if isinstance(item, dict)]
        summary = value.get("summary") if isinstance(value.get("summary"), dict) else {}
        if summary.get("gate_count") != len(gates):
            errors.append("benchmark.summary.gate_count: value does not match quality gate count")
        if summary.get("passed") != statuses.count("pass"):
            errors.append("benchmark.summary.passed: value does not match passing gate count")
        if summary.get("failed") != statuses.count("fail"):
            errors.append("benchmark.summary.failed: value does not match failed gate count")
        if summary.get("warnings") != statuses.count("warn"):
            errors.append("benchmark.summary.warnings: value does not match warning gate count")
        expected_overall = "failed" if "fail" in statuses else ("needs-review" if "warn" in statuses else "passed")
        if summary.get("overall_status") != expected_overall:
            errors.append("benchmark.summary.overall_status: value does not match gate statuses")
    else:
        errors.append("benchmark.quality_gates: expected array")
    walk(value, "benchmark")
    for finding in iter_secret_findings(value, field_path="benchmark"):
        errors.append(finding)
    return errors


def context_text(ctx: dict[str, Any], key: str, default: str = "") -> str:
    value = ctx.get(key)
    if value is None or value == "":
        return default
    return str(value)


def build_benchmark(
    *,
    lab_root: Path,
    run_dir: Path,
    fixture: str | None = None,
    max_chains: int = DEFAULT_MAX_CHAINS,
    validate_reports: bool = True,
) -> dict[str, Any]:
    run_dir = run_dir.resolve()
    if not run_dir.is_dir():
        raise BenchmarkError(f"benchmark run directory not found: {run_dir}")
    if not (run_dir / "context.json").is_file():
        raise BenchmarkError(f"benchmark run context not found: {run_dir / 'context.json'}")
    ctx = load_context(run_dir)
    reports = reports_dir(run_dir)
    reports.mkdir(parents=True, exist_ok=True)
    metrics, metrics_meta = load_or_build_metrics(run_dir, reports)
    validation = run_validation(lab_root, run_dir) if validate_reports else {"exit_code": 0, "error_count": 0, "validated": True}
    pattern_matches = summarize_pattern_matches(run_dir, reports)

    adv = metrics.get("adversarial_validation") if isinstance(metrics.get("adversarial_validation"), dict) else {}
    chains = metrics.get("chains") if isinstance(metrics.get("chains"), dict) else {}
    proofs = metrics.get("proofs") if isinstance(metrics.get("proofs"), dict) else {}
    issue_plan = metrics.get("issue_publication_plan") if isinstance(metrics.get("issue_publication_plan"), dict) else {}
    artifacts = metrics.get("artifacts") if isinstance(metrics.get("artifacts"), dict) else {}
    workflow_profile = metrics.get("workflow_profile") if isinstance(metrics.get("workflow_profile"), dict) else {}
    proofs_data = load_json(reports / "proofs.json", {}) or {}
    unsafe_proof_count = count_unsafe_proof_records(proofs_data) + count_rejected_patch_validation_commands(reports)
    existing_publications = count_existing_publications(reports)
    visibility = str(ctx.get("visibility") or "UNKNOWN").upper()
    public_publications = existing_publications if visibility == "PUBLIC" else 0

    chain_total = safe_int(chains.get("total"))
    issue_warning_count = safe_int(issue_plan.get("warning_count"))
    downgrade_rate = safe_float(adv.get("downgrade_or_invalidate_rate"))
    skipped_by_scope_count = safe_int(workflow_profile.get("skipped_by_scope_count"))

    gates = [
        gate(
            "report-validation",
            "Report validation passes",
            "pass" if validation["validated"] else "fail",
            validation["exit_code"],
            0,
            "gra-validate-report completed successfully." if validation["validated"] else "gra-validate-report returned non-zero; inspect local validation output.",
        ),
        gate(
            "secret-scan",
            "No generated report artifact contains obvious full secrets",
            "pass" if pattern_matches["match_count"] == 0 else "fail",
            pattern_matches["match_count"],
            0,
            "No obvious unredacted full secret patterns were found in local report artifacts."
            if pattern_matches["match_count"] == 0
            else "Obvious full secret patterns were detected; paths are listed without copying secret values.",
            list(pattern_matches["paths"]),
        ),
        gate(
            "adversarial-downgrade-rate",
            "Adversarial validation downgrade/invalidate rate is recorded",
            "pass",
            downgrade_rate,
            "recorded",
            f"Rate source: {metrics_meta['source']}.",
        ),
        gate(
            "chain-count",
            "Chain count is bounded",
            "pass" if chain_total <= max_chains else "fail",
            chain_total,
            max_chains,
            "Chain synthesis output is within the configured bound."
            if chain_total <= max_chains
            else "Chain synthesis output exceeds the configured bound and should be triaged for noise.",
        ),
        gate(
            "proof-unsafe-rejections",
            "Proof unsafe rejection count is recorded",
            "pass" if unsafe_proof_count == 0 else "warn",
            unsafe_proof_count,
            "recorded",
            "No unsafe proof or patch-validation command rejections were observed."
            if unsafe_proof_count == 0
            else "Unsafe proof or patch-validation command rejections were observed; review before relying on proof coverage.",
        ),
        gate(
            "issue-plan-warnings",
            "Issue plan warning count is recorded",
            "pass" if issue_warning_count == 0 else "warn",
            issue_warning_count,
            "recorded",
            "No issue publication plan warnings were recorded."
            if issue_warning_count == 0
            else "Issue publication plan warnings are present; resolve before publication.",
        ),
        gate(
            "no-public-issue-apply",
            "No public Issue apply is performed by benchmark",
            "fail" if public_publications else ("warn" if existing_publications else "pass"),
            public_publications if public_publications else existing_publications,
            0,
            "gra-benchmark does not call gra-issues --apply and no existing publication ledger entries were detected."
            if not existing_publications
            else "Existing Issue publication ledger entries were detected; benchmark did not create them.",
        ),
    ]
    summary = summarize_gates(gates)
    benchmark = {
        "schema_version": "1",
        "run_id": context_text(ctx, "run_id", run_dir.name),
        "repo": context_text(ctx, "repo"),
        "branch": context_text(ctx, "branch"),
        "commit": context_text(ctx, "commit"),
        "generated_at": utc_now(),
        "source": "local-benchmark",
        "fixture": {
            "name": fixture,
            "run_dir": rel_to_base(lab_root, run_dir),
            "network_allowed": bool(ctx.get("network_allowed", False)),
        } if fixture else None,
        "safety": {
            "local_artifacts_only": True,
            "network_accessed": False,
            "issue_apply_performed": False,
            "raw_evidence_copied": False,
            "secrets_copied": False,
            "bounded_summaries_only": True,
            "notes": "Benchmark records counts, rates, statuses, and artifact paths only; raw evidence and issue bodies are not copied.",
        },
        "metrics": {
            **metrics_meta,
            "summary": {
                "findings_total": safe_int((metrics.get("findings") or {}).get("total") if isinstance(metrics.get("findings"), dict) else 0),
                "issue_recommended_findings": safe_int((metrics.get("findings") or {}).get("issue_recommended") if isinstance(metrics.get("findings"), dict) else 0),
                "adversarial_validation_total": safe_int(adv.get("total")),
                "adversarial_downgrade_or_invalidate_rate": downgrade_rate,
                "chain_count": chain_total,
                "proof_count": safe_int(proofs.get("total")),
                "proof_unsafe_rejection_count": unsafe_proof_count,
                "issue_plan_warning_count": issue_warning_count,
                "manifest_hygiene_warnings": safe_int(artifacts.get("manifest_hygiene_warnings")),
                "workflow_profile": str(workflow_profile.get("profile") or "not-recorded"),
                "workflow_skipped_by_scope_count": skipped_by_scope_count,
            },
        },
        "quality_gates": gates,
        "summary": summary,
        "follow_up_actions": follow_up_actions(gates, metrics_meta),
    }
    errors = validate_benchmark_payload(benchmark)
    if errors:
        raise BenchmarkError("benchmark payload failed internal validation: " + "; ".join(errors[:5]))
    return benchmark


def render_benchmark_markdown(benchmark: dict[str, Any]) -> str:
    metrics = benchmark.get("metrics", {}) if isinstance(benchmark.get("metrics"), dict) else {}
    metrics_summary = metrics.get("summary", {}) if isinstance(metrics.get("summary"), dict) else {}
    summary = benchmark.get("summary", {}) if isinstance(benchmark.get("summary"), dict) else {}
    lines = [
        "# Benchmark Report",
        "",
        "Local-only quality gates for a GenAI Repo Auditor run.",
        "This report intentionally excludes raw finding evidence, issue body text, proof payloads, scanner lead bodies, and secret values.",
        "",
        f"- Run ID: `{benchmark.get('run_id', '')}`",
        f"- Repository: `{benchmark.get('repo', '')}`",
        f"- Commit: `{benchmark.get('commit', '')}`",
        f"- Generated at: `{benchmark.get('generated_at', '')}`",
        f"- Overall status: `{summary.get('overall_status', '')}`",
        f"- Metrics source: `{metrics.get('source', '')}`",
        "",
        "## Metrics summary",
        "",
        "| Metric | Value |",
        "|---|---:|",
        f"| Findings | {metrics_summary.get('findings_total', 0)} |",
        f"| Issue-recommended findings | {metrics_summary.get('issue_recommended_findings', 0)} |",
        f"| Adversarial validations | {metrics_summary.get('adversarial_validation_total', 0)} |",
        f"| Downgrade / invalidate rate | {metrics_summary.get('adversarial_downgrade_or_invalidate_rate', 0.0):.4f} |",
        f"| Chains | {metrics_summary.get('chain_count', 0)} |",
        f"| Proofs | {metrics_summary.get('proof_count', 0)} |",
        f"| Unsafe proof rejections | {metrics_summary.get('proof_unsafe_rejection_count', 0)} |",
        f"| Issue plan warnings | {metrics_summary.get('issue_plan_warning_count', 0)} |",
        f"| Manifest hygiene warnings | {metrics_summary.get('manifest_hygiene_warnings', 0)} |",
        f"| Workflow skipped by scope | {metrics_summary.get('workflow_skipped_by_scope_count', 0)} |",
        "",
        "## Quality gates",
        "",
        "| Gate | Status | Value | Threshold | Details |",
        "|---|---|---:|---|---|",
    ]
    for item in benchmark.get("quality_gates") or []:
        if not isinstance(item, dict):
            continue
        lines.append(
            f"| {item.get('title', '')} | {item.get('status', '')} | {item.get('value', '')} | "
            f"{item.get('threshold', '')} | {item.get('details', '')} |"
        )
    lines.extend(["", "## Follow-up actions", ""])
    for action in benchmark.get("follow_up_actions") or []:
        lines.append(f"- {action}")
    lines.extend([
        "",
        "## Safety notes",
        "",
        "- The benchmark does not access external networks.",
        "- The benchmark does not call `gra-issues --apply` or create GitHub Issues.",
        "- Listed artifact paths are local run-relative references for operator review.",
        "",
    ])
    return "\n".join(lines)


def write_benchmark(
    *,
    lab_root: Path,
    run_dir: Path,
    fixture: str | None = None,
    max_chains: int = DEFAULT_MAX_CHAINS,
    out_json: Path | None = None,
    out_md: Path | None = None,
    validate_reports: bool = True,
) -> tuple[Path, Path, dict[str, Any]]:
    reports = reports_dir(run_dir.resolve())
    benchmark = build_benchmark(
        lab_root=lab_root,
        run_dir=run_dir,
        fixture=fixture,
        max_chains=max_chains,
        validate_reports=validate_reports,
    )
    json_path = out_json.resolve() if out_json else reports / "benchmark.json"
    md_path = out_md.resolve() if out_md else reports / "BENCHMARK.md"
    write_json(json_path, benchmark)
    md_path.parent.mkdir(parents=True, exist_ok=True)
    md_path.write_text(render_benchmark_markdown(benchmark), encoding="utf-8")
    return json_path, md_path, benchmark
